# Arithmatics operator
print(5+2)
print(5-2)
print(5*2)
print(5/2)
print(5//2)# it gives us integer value of result. Ex- 5/2=2.5 but it gives only 2.
print(5%2)# it gives remainder.
print(5**2)# it gives us power like as 5 ke power 2 = 25.

