# Comparison Operator.
print(3>2)
print(3<2)
print(3>=2)
print(3<=2)
print(3==3)
print(3!=2)
# Logical Operator
print(3>2 or 2>3)
print(3>2 and 4<3)
print(not 3>2) # --> 3>2 is true but result show false because of not operator.

